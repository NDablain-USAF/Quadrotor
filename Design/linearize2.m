function [A,B] = linearize2(x,Ixx,Iyy,Izz)
A = zeros(6,6,1);
B = zeros(6,3,1);
for i = 1:1
    A(:,:,i) = [0 x(6) 0 1 0 x(2);...
        -x(6) 0 0 0 1 -x(1);...
        x(5) 0 0 0 x(1) 1;...
        0 0 0 0 ((Iyy-Izz)/Ixx)*x(6) ((Iyy-Izz)/Ixx)*x(5);...
        0 0 0 ((Izz-Ixx)/Iyy)*x(6) 0 ((Izz-Ixx)/Iyy)*x(4);...
        0 0 0 ((Ixx-Iyy)/Izz)*x(5) ((Ixx-Iyy)/Izz)*x(4) 0];
    B(:,:,i) = [zeros(3,3);...
        1/Ixx 0 0;...
        0 1/Iyy 0;...
        0 0 1/Izz];
end
end
